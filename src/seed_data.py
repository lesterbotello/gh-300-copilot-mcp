def seed_database() -> None:
    """Populate the database with the starter activities and participants."""
    Base.metadata.create_all(bind=engine)

    session = SessionLocal()
    try:
        if session.scalar(select(Activity).limit(1)) is not None:
            return

        for activity_name, details in INITIAL_ACTIVITIES.items():
            session.add(
                Activity(
                    name=activity_name,
                    description=details["description"],
                    schedule=details["schedule"],
                    max_participants=details["max_participants"],
                )
            )

            for participant_email in details["participants"]:
                if session.get(User, participant_email) is None:
                    session.add(User(email=participant_email))
                session.add(Registration(activity_name=activity_name, email=participant_email))

        session.commit()
    finally:
        session.close()
